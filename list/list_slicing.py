#list slicing have three critria seperated by colon :
#first critria used to slice list from
#second critria used to slice list from index to given index
#third critria used to increment or decrement when slicing

l=[4,7,6,2,8,3]

print(l[2:]) #first critria

print(l[1:3]) #first and second critria

print(l[:4])

print(l[0::2])

print(l[-1::-1]) #reverse the list item
