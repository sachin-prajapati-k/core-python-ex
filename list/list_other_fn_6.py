#count function
#it counts the given value in the list and return the no. of repeated time
l=[10,20,10,40,10]
a=l.count(10)
print(a)

#in string count function
l1=['s','a','c','h','i','n','i','s']
b=l1.count('i')
print(b)

#max function in list
l2=[10,90,40,60,20,55]
d=max(l2)
print(d)

#in string max function
l4=["sachin","prajapati"]
e=max(l4)     # it find the max word according to ascii code and then return
print(e)      # the highest code value

#min function in list
l5=[10,550,660,30,20]
f=min(l5)
print(f)

#in string min function
g=min(l4)
print(g)

#sort function in the list
l6=[5,7,8,3,4,1,9,5,10,12]
l6.sort()
